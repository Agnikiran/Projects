abstract class DriverDetailsInterface {
  final String _name = "name";
  final String _likes = "likes";
  final String _comments = "comments";
  final String _rating = "rating";
  final String _mobile = "mobile";
  final String _email = "email";

  get name => _name;
  get likes => _likes;
  get comments => _comments;
  get rating => _rating;
  get mobile => _mobile;
  get email => _email;
}
