abstract class SignupScreenInterface {
  final String _name = "name";
  final String _email = "email";
  final String _mobile = "mobile";
  final String _password = "password";
  final String _confPassword = "confPassword";
  final String _visitType = "visitType";

  get name => _name;
  get email => _email;
  get mobile => _mobile;
  get password => _password;
  get confPassword => _confPassword;
  get visitType => _visitType;
}
